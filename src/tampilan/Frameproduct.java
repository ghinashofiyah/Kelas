        void loadTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID PRODUK");
        model.addColumn("PRODUK");
        model.addColumn("DESKRIPSI");
        model.addColumn("HARGA");
        model.addColumn("KATEGORI");

        try {
            product pro = new product();
            ResultSet data = pro.tampilProduk();

            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("ID"),
                    data.getString("Nama"),
                    data.getString("Deskripsi"),
                    data.getString("Harga"),
                    data.getString("Kategori"),});

            }

        } catch (SQLException sQLException) {

        }

        dproduct.setModel(model);
    }

    
    void reset() {
        autoID();
        bID.setEditable(true);
        bDeskripsi.setText(null);
        bHarga.setText(null);
        xKategori.setSelectedItem(null);
        bNama.setText(null);
        
    }
    
    
    void comboBox() {
        
        try {
            category cat = new category();
            ResultSet data = cat.tampilcomboBox();
            while (data.next()) {
                String isi = data.getString("category_name");
                xKategori.addItem(isi);
            }
        } catch (SQLException ex) {
        }
    }
    
    void autoID() {

        try {
            product pro = new product();
            ResultSet id = pro.autoID();

            if (id.next()) {
                int auto = id.getInt("ID") + 1;
                bID.setText(String.valueOf(auto));
            } else {
                bID.setText("1");
            }

        } catch (SQLException sQLException) {
        }
    }

    
    
    

