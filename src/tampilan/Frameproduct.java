    private void dproductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dproductMouseClicked
        // TODO add your handling code here:
        int baris = dproduct.rowAtPoint(evt.getPoint());
        String ID = dproduct.getValueAt(baris, 0).toString();
        String Nama = dproduct.getValueAt(baris, 1).toString();
        String Desc = dproduct.getValueAt(baris, 2).toString();
        String Harga = dproduct.getValueAt(baris, 3).toString();
        String Kategori = dproduct.getValueAt(baris, 4).toString();
        
        bID.setText(ID);
        bID.setEditable(false);
        bNama.setText(Nama);
        bHarga.setText(Harga);
        bDeskripsi.setText(Desc);
        xKategori.setSelectedItem(Kategori);
    }//GEN-LAST:event_dproductMouseClicked

    private void tTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tTambahActionPerformed
        // TODO add your handling code here:
        try {
            product pro = new product();
            category cat = new category();
            pro.setProduct_id(Integer.parseInt(bID.getText()));
            pro.setProduct_name(bNama.getText());
            pro.setProduct_desc(bDeskripsi.getText());
            pro.setProduct_price(Integer.parseInt(bHarga.getText()));
            cat.setCategory_name(xKategori.getSelectedItem().toString());
                        ResultSet data = cat.Konversi();
                        
                        if (data.next()) {
                            String isi = data.getString("category_id");
                            pro.setProduct_cat_id(Integer.parseInt(isi));
                
            }            
            pro.tambahProduk();
        } catch (SQLException sQLException) {
            System.out.println("data tidak masuk");
        }
        
        loadTable();
        reset();
        
    }//GEN-LAST:event_tTambahActionPerformed

